package com.hy.Sheji.bean;

public class Address {

	private Integer addId;
	private Integer addUid;
	private String addAddr;
	private String addPhone;
	private String addName;
	private Integer addDft;
	public Integer getAddId() {
		return addId;
	}
	public void setAddId(Integer addId) {
		this.addId = addId;
	}
	public Integer getAddUid() {
		return addUid;
	}
	public void setAddUid(Integer addUid) {
		this.addUid = addUid;
	}
	public String getAddAddr() {
		return addAddr;
	}
	public void setAddAddr(String addAddr) {
		this.addAddr = addAddr;
	}
	public String getAddPhone() {
		return addPhone;
	}
	public void setAddPhone(String addPhone) {
		this.addPhone = addPhone;
	}
	public String getAddName() {
		return addName;
	}
	public void setAddName(String addName) {
		this.addName = addName;
	}
	public Integer getAddDft() {
		return addDft;
	}
	public void setAddDft(Integer addDft) {
		this.addDft = addDft;
	}
	 
}
